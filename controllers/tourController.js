exports.getTourStats = async (req, res) => {
  try {
    const stats = await Tour.aggregate([
      {
        $match: { rating: { $gte: 4.5 } },
      },
      {
        $group: {
          _id: { $toUpper: '$difficulty' },
          numTours: { $sum: 1 }, // add 1 for each document
          numRatings: { $sum: '$rating' },
          avgRating: { $avg: '$rating' },
          avgPrice: { $avg: '$price' },
          minPrice: { $min: '$price' },
          maxPrice: { $max: '$price' },
        },
      },
      {
        // sort by avgPrice
        $sort: { avgPrice: 1 },
      },
      {
        // remove easy tours
        $match: { _id: { $ne: 'EASY' } },
      },
    ]);

    console.log(stats);

    res.status(200).json({
      status: 'success',
      data: {
        stats,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

exports.getMonthlyPlan = async (req, res) => {
  try {
    const year = req.params.year * 1;
    const plan = await Tour.aggregate([
      {
        // unwind deconstructs an array field from the input documents to output a document for each element
        $unwind: '$startDates',
      },
      {
        // match the year
        $match: {
          startDates: {
            $gte: new Date(`${year}-01-01`), // greater than or equal to
            $lte: new Date(`${year}-12-31`), // less than or equal to
          },
        },
      },
      {
        // group by month
        $group: {
          _id: { $month: '$startDates' }, // group by month
          numTourStarts: { $sum: 1 }, // add 1 for each document
          tours: { $push: '$name' }, // create an array of tour names
        },
      },
      {
        // add a new field
        $addFields: { month: '$_id' },
      },
      {
        // remove the _id field
        $project: {
          _id: 0,
        },
      },
      {
        // sort by numTourStarts
        $sort: { numTourStarts: -1 },
      },
      {
        // limit the output to 12 documents
        $limit: 12,
      },
    ]);

    res.status(200).json({
      status: 'success',
      data: {
        plan,
      },
    });
  } catch (err) {
    res.status(400).json({
      status: 'fail',
      message: err,
    });
  }
};

